package modele;

public class Personnel {
	private String nom;
	private String prenom;
	private String tel;
	private String mail;
	
	public Personnel() {
		
	}
	
	public String getNom() {
		return this.nom;
	}
	
	public String getPrenom() {
		return this.prenom;
	}
	
	public String getTel() {
		return this.tel;
	}
	public String getMail() {
		return this.mail;
	}
}
